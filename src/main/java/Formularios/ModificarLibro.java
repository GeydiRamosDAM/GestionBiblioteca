    void cargarDatosFormulario() throws SQLException
    {
        
         myConnDB.connect();
        
        ResultSet libro = myConnDB.query("SELECT * FROM libros where idLibros = '" + idLibroModificar +"'");
        while(libro.next())
            {
               
                nombre = libro.getString("Nombre");
                autor = libro.getString("Autor");
                edicion = libro.getString("Edicion");
                anno = libro.getString("Anno");
                stock = libro.getString("Stock");

                
              
                //cargar datos en el formulario
                jt_nombre.setText(nombre);
                jt_autor.setText(autor);
                jt_edicion.setText(edicion);
                jt_anno.setText(anno);
                jt_stock.setText(stock);

           }

         myConnDB.disconnect();
        
    
    
    }
    
