    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // toma de datos del formulario e inserción en BD
        nombre = jt_nombre.getText();
        autor = jt_autor.getText();
        edicion = jt_edicion.getText();
        anno = jt_anno.getText();
        stock = jt_stock.getText();
        
        //validacion de datos
        
        if (nombre.hashCode() == 0 || autor.hashCode() == 0 || edicion.hashCode() == 0 || anno.hashCode() == 0 || stock.hashCode() == 0 )
        {
            //jOptionPane1.setVisible(true);
            jOptionPane1.showMessageDialog(null, "Uno o varios campos están vacios!");
        }
        else
        {
            
            try {
                //conecto con BD
                myConnDB.connect();
                
               
                //actualizar en BD --- updateQuery() devuelve 1 si modificó algún registro en BD, 0 sino de devuelve o modifica un registro
                
                    String query =               
                        "UPDATE libros SET" +
                        " Nombre = '" + nombre + "'," +
                        " Autor = '" + autor + "'," +
                        " Edicion = '" + edicion + "'," +
                        " Anno = '" + anno + "'," +
                        " Stock = '" + stock + "'" +  
                        " WHERE idLibros = " + idLibroModificar;   
                
                   
                
                 actualizarLibro = myConnDB.updateQuery(query);
                
                //desconecto con BD
                myConnDB.disconnect();
                
                jOptionPane1.showMessageDialog(null, "Se ha modificado correctamente el libro!");
                this.setVisible(false);
               
                                
                } catch (Exception e) {
                   // throw new Exception("");
                }
            
            
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

