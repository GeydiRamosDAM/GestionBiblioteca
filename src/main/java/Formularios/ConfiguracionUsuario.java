    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // actualizar datos en BD
        char [] inputContrasenna = jp_contrasenna.getPassword();
        char [] inputRepContrasenna = jp_repContrasenna.getPassword();
        
        String rolSelecionadoFormulario = jcb_listaRoles.getSelectedItem().toString();
                
        
        switch (rolSelecionadoFormulario) {
            case "Cliente":
                rolSelecionadoFormulario = "2";
                break;
            case "Administrador":
                rolSelecionadoFormulario = "1";
                break;
            case "Gestor":
                rolSelecionadoFormulario = "3";
                break;
            default:
                rolSelecionadoFormulario = "-1";
        }
        
        
        if (rolSelecionadoFormulario.equals("-1") || jt_nombre.getText().hashCode() == 0 || jt_apellidos.getText().hashCode() == 0 || jt_telef.getText().hashCode() == 0 || jt_ciudad.getText().hashCode() == 0 || jt_pais.getText().hashCode() == 0 )
        {
            //jOptionPane1.setVisible(true);
            jOptionPane1.showMessageDialog(null, "Uno o varios campos están vacios!");
        }
        else if(!Arrays.equals(inputContrasenna, inputRepContrasenna))
        {
            //jOptionPane1.setVisible(true); 
            jOptionPane1.showMessageDialog(null, "Las contraseñas no coinciden!");
        }
        else
        {
             try {
                    //conecto con BD
                    myConnDB.connect();

                    String contrasennaEncriptada = Encriptacion.javaEncode(new String(inputContrasenna));

                    //actualiza en BD --- updateQuery() devuelve 1 si modificó algún registro en BD, 0 sino de devuelve o modifica un registro
                    String query =               
                        "UPDATE usuarios SET" +
                        " Nombre = '" + jt_nombre.getText() + "'," +
                        " Apellido = '" + jt_apellidos.getText() + "'," +
                        " Telefono = '" + jt_telef.getText() + "'," +
                        " Ciudad = '" + jt_ciudad.getText() + "'," +
                        " Pais = '" + jt_pais.getText() + "'," +
                        " Rol = '" + rolSelecionadoFormulario + "'," +
                        " contrasenna = '" + contrasennaEncriptada + "'" +    
                        " WHERE idUsuario = " + idUsuario;        
                        

                     int actualizacionUsuario = myConnDB.updateQuery(query);

                    //desconecto con BD
                    myConnDB.disconnect();

                    jOptionPane1.showMessageDialog(null, "Se ha actualizado correctamente el usuario!");
                    this.setVisible(false);
    //                InicioSesion iniSesion = new InicioSesion();
    //                iniSesion.setVisible(true);

                    } catch (SQLException e) {
                       // throw new Exception("");
                    }
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

