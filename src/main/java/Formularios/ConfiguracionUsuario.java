    public void cargarDatosEnFormulario() throws SQLException
    {
        //traer datos de BD del usuario
        
        myConnDB.connect();
        
        ResultSet usuario = myConnDB.query("SELECT * FROM usuarios where idUsuario = '" + idUsuario +"'");
        while(usuario.next())
            {
               
                nombre = usuario.getString("Nombre");
                apellidos = usuario.getString("Apellido");
                correoUsuario = usuario.getString("CorreoElectronico");
                telef = usuario.getString("Telefono");
                ciudad = usuario.getString("Ciudad");
                pais = usuario.getString("Pais");
                contrasennaEncriptada = usuario.getString("contrasenna");
                rolSelecionadoFormulario = String.valueOf(usuario.getInt("Rol"));
                
              
                //cargar datos en el formulario
                jt_nombre.setText(nombre);
                jt_apellidos.setText(apellidos);
                jt_correoUsuario.setText(correoUsuario);
                jt_telef.setText(telef);
                jt_ciudad.setText(ciudad);
                jt_pais.setText(pais);
                
                jcb_listaRoles.setSelectedIndex(usuario.getInt("Rol")-1);
               
                contrasennaDesencriptada = Encriptacion.javaDecode(contrasennaEncriptada);
                
                jp_contrasenna.setText(contrasennaDesencriptada);
                jp_repContrasenna.setText(contrasennaDesencriptada);
            }

         myConnDB.disconnect();
        
        
    }

